
public class S06_CharAndooleans {
	
	public static void main(String[] args) {
		
		char myChar = 'D';
		System.out.println("myChar: " + myChar);
		 
		int i = myChar;
		System.out.println(i);
		
		char x = 67;
		System.out.println("char x: " + x);
		
		char mynewchar = '\u0044';
		System.out.println(mynewchar);
		
		mynewchar = '\u0055';
		System.out.println(mynewchar);
		
		mynewchar = '\u1144';
		System.out.println(mynewchar);
		
		mynewchar = '\u1324';
		System.out.println(mynewchar);
		
		boolean flag = true;
		System.out.println(flag);
		
		boolean istrue = true;
		System.out.println(istrue);
		
		
	}

}
